"""
13.	Перемістити від’ємні елементи парних рядків наліво не міняючи їх порядок
"""
import math
a = [
    [1, 4, 7, -8],
    [1, 2, 4, 7],
    [6, -24, 2, 1],
    [-4, 2, 5, 1]
]
for i in range(0, len(a), 2):
    for j in range(len(a[i])):
        if a[i][j] < 0:
            t = a[i][j-1]
            a[i][j-1] = a[i][j]
            a[i][j] = t
print(a)